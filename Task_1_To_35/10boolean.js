// 10. Boolean value is either true or false.
//     - Write three JavaScript statement which provide truthy value.
//     - Write three JavaScript statement which provide falsy value.

// Core Concept : In JavaScript, a truthy value is a value that is considered true when encountered in a Boolean context. All values are truthy unless they are defined as falsy. That is, all values are truthy except false, 0, -0, 0n, "", null, undefined, and NaN which are falsy values.

// Truthy Values :

console.log(10>9);

console.log('cat' && 'dog');

console.log(34%6);

// Falsy Values

console.log(-5>100);

console.log(100%2);

let a;
console.log(a);

